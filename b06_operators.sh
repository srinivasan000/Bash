echo "5+5= `expr 5 + 5`";   #-> 5+5= 10
echo "5*5= $((5*5))";   # ->5*5= 25
echo "2^2= $((2**2))";  # -> 2^2= 4
a=3;
echo $((a++));  # -> 3
echo $a     # -> 4
echo $((++a))   # -> 5

echo $((a+=10));    # -> 15

[ 5 -lt 6 ] && echo "true" || echo "false" # -> true

(( 5<=6 )) && echo "true" || echo "false" # -> true

echo $(( 5 == 9 )); # -> 0